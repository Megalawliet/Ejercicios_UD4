package EjercicosVarios;

public class Ejercicio3 {
	
	public static void main(String[] args) {
		//Declaración de variables		
		int X = 85;
		int Y = 43;
		double N = 0.5;
		double M = 10.3;
		//Ejecucion en pantalla de las operaciones
		System.out.println("El valor de cada variable es: " +X+" "+Y);
		System.out.println("La suma de X+Y es: "+(X+Y));
		System.out.println("La diferencia X-Y es: "+(X-Y));
		System.out.println("El producto X*Y es: "+(X*Y));
		System.out.println("El cociente X/Y es: "+(X/Y));
		System.out.println("El resto X%Y es: "+(X%Y));
		System.out.println("La suma de N+M es:"+(N+M));
		System.out.println("La diferencia de N-M es:"+(N-M));
		System.out.println("El producto de N*M es:"+(N*M));
		System.out.println("El cociente de N/M es:"+(N/M));
		System.out.println("El resto de N%M es:"+(N%M));
		System.out.println("La suma de X+N es: "+(X+N));
		System.out.println("La diferencia Y/M es: "+(Y/M));
		System.out.println("El resto Y%M es: "+(Y%M));
		System.out.println("El doble de cada variable es: "+(X*2)+"  "+(Y*2)+"  "+(N*2)+"  "+(M*2));
		System.out.println("La suma de todas las variables: "+(X+Y+N+M));
		System.out.println("El producto de todas las variables es: "+(X*Y*N*M));		
	}
}
